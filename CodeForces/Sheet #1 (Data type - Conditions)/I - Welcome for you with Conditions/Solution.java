import java.util.*;
 
public class Solution {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int numberOne = scan.nextInt();
        int numberTwo = scan.nextInt();
        if (numberOne >= numberTwo) {
            System.out.print("Yes");
        } else {
            System.out.print("No");
        }
 
    }}